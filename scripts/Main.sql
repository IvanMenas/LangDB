-- GET_ID_USER Function
----------------------------------------------------------
BEGIN
   EXECUTE IMMEDIATE 'DROP FUNCTION GET_ID_USER';
EXCEPTION WHEN OTHERS THEN
   IF SQLCODE != -4043 THEN
      RAISE;
   END IF;
END;

CREATE OR REPLACE FUNCTION GET_ID_USER (
    P_DATA IN VARCHAR2,
    P_DATA2 IN VARCHAR2,
    P_BY IN VARCHAR2
) RETURN NUMBER IS
    V_ID_USER  USUARIOS.ID_USER%TYPE := 0;
BEGIN
    CASE P_BY
        WHEN 'ID_USER' THEN
            SELECT
                ID_USER
            INTO
                V_ID_USER
            FROM
                USUARIOS
            WHERE
                ID_USER = TO_NUMBER(P_DATA);
                
            RETURN (V_ID_USER);
        WHEN 'USERNAME' THEN
            SELECT
                ID_USER
            INTO
                V_ID_USER
            FROM
                USUARIOS
            WHERE
                USERNAME = P_DATA;
                
            RETURN (V_ID_USER);
        WHEN 'PASSWORD' THEN
            SELECT
                ID_USER
            INTO
                V_ID_USER
            FROM
                USUARIOS
            WHERE
                    USERNAME = P_DATA
                AND PASSWORD = P_DATA2;
                
            RETURN (V_ID_USER);
    END CASE;
    
    EXCEPTION WHEN NO_DATA_FOUND THEN
        RETURN (V_ID_USER);
END;

